        OAI_grid.(region{n}).lon,OAI_grid.(region{n}).TA,...
        flipud(slanCM('romao')),'TA',...
        'Surface {\itA}_{T} (\mumol kg^{-1})',...
        region{n},lme_shape,lme_idx);

    % plot estimated phosphate
    plot_temporal_mean(OAI_grid.(region{n}).lim,...
