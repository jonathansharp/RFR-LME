%% create regional animations of RFR-LME indicators
addpath(genpath(pwd));
% this script defines the bounds of the eleven LMEs
define_regions_eiwg

for n = 8%1:length(region)

    % load gridded fCO2, predictors, and models
    load(['Data/' region{n} '/ML_fCO2'],'OAI_grid');

    % plot estimated fCO2
    plot_temporal_mean(OAI_grid.(region{n}).lim,...
        OAI_grid.(region{n}).dim,OAI_grid.(region{n}).lat,...
        OAI_grid.(region{n}).lon,OAI_grid.(region{n}).fCO2,...
        flipud(slanCM('romao')),'fCO2','Surface {\itf}CO_{2} (\muatm)',...
        region{n},lme_shape,lme_idx);

    % plot estimated pCO2
    plot_temporal_mean(OAI_grid.(region{n}).lim,...
